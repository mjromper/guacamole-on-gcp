# !/bin/bash

FILE=./client/openid-secrets.properties


if [ -z ${1} ]; then
    echo "Usage: bin/create-openid-secrets.sh [OPENID_CLIENT_ID] [OPENID_REDIRECT_URI] [OPENID_AUTHORIZATION_ENDPOINT] [OPENID_JWKS_ENDPOINT] [OPENID_ISSUER]"
    exit 1
else
    OPENID_CLIENT_ID=${1}
    OPENID_REDIRECT_URI=${2}
    OPENID_AUTHORIZATION_ENDPOINT=${3}
    OPENID_JWKS_ENDPOINT=${4}
    OPENID_ISSUER=${5}

    echo "# Auto generated by create-openid-secrets.sh" > ${FILE}

    echo "OPENID_AUTHORIZATION_ENDPOINT=$OPENID_AUTHORIZATION_ENDPOINT" >> ${FILE}
    echo "OPENID_JWKS_ENDPOINT=$OPENID_JWKS_ENDPOINT" >> ${FILE}
    echo "OPENID_ISSUER=$OPENID_ISSUER" >> ${FILE}
    echo "OPENID_CLIENT_ID=$OPENID_CLIENT_ID" >> ${FILE}
    echo "OPENID_REDIRECT_URI=$OPENID_REDIRECT_URI" >> ${FILE}

    exit 0
fi